# 4. You are given two integer variables x and y. You need to perform the following operations:
# p = x + y : Addition
# q = x - y : Subtraction
# r = x * y :Multiplication
# s = x / y : Division
# t = x % y : Modulo

x = int(input("Enter variable x: "))
y = int(input("Enter variable y: "))

p = x + y
q = x - y
r = x * y
s = x / y
